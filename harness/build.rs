fn main() {
    let var_host = env::var("HOST").unwrap();
    let var_target = env::var("TARGET").unwrap();
    println!("cargo:rustc-env=RUSTC_HOST={var_host}");
    println!("cargo:rustc-env=RUSTC_TARGET={var_target}");
    println!("cargo:rerun-if-changed-env=TARGET");
}
